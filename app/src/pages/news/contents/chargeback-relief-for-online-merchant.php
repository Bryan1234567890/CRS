                <h1 class="heading">iCAN4Consumers Service Provides Chargeback Relief for Online Merchants</h1>
            </div>

            <div class="col-sm-12">

                <p class="text">
                    ATLANTA, GA–(Marketwired – September 22, 2015) – Today <span class="bold">iCAN4Consumers</span> an eC Consulting Services LLC company, officially announced its newest service offering, <span class="bold">iCAN4Consumers</span>.
                </p>

                <p class="text"><span class="bold">iCAN4ConsumersiCAN4Consumers</span> is a multifaceted service platform designed to reduce fraud and chargebacks for e-commerce merchants. The <span class="bold">iCAN4C</span> service provides a mechanism to facilitate a quick, easy, and hassle-free refund to the consumer, thereby eliminating the</p>

                <p class="text"><span class="bold">iCAN4C</span> serves as a merchant’s customer service backstop against a consumer’s tendency to chargeback a purchase when dissatisfied. Consequently, the merchant avoids the fees and other issues related to the chargeback, is able to reduce its fraud ratios, stay in compliance with those critical association thresholds, and maintain its merchant account.</p>

                <p class="text">For consumers, the <span class="bold">iCAN4C</span> platform provides a streamlined method to obtain a hassle-free refund for unwanted or defective services or merchandise, achieving a satisfactory resolution to any dispute.</p>

                <p class="text">The <span class="bold">iCAN4C</span> platform is designed to enhance a merchant’s ability to reduce chargebacks, and complement any other services that a merchant may be using. According to Jock West, CEO for eC Consulting, “The <span class="bold">iCAN4C</span> service is unique in that our strategy for avoiding chargebacks is vastly different from any other service in that disputes resolved through the <span class="bold">iCAN4C</span> platform are between the merchant and the consumer — the issuing bank is never involved.”</p>

                <p class="text">Mark Garfinkel, eC Consulting Services’ COO states, “<span class="bold">iCAN4Consumers</span> brings together a “community of consumers” and a “network of merchants” working together to resolve disputes, rather than incur the additional costs associated with chargeback resolution.iCAN4C’s goal is to reduce the cost of doing business for both consumers and merchants across the entire ecommerce ecosystem.”</p>

