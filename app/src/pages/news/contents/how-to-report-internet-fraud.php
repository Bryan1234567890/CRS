            <div class="col-sm-12">
                <h2 class="heading">How to Report Internet Fraud</h2>
            </div>

            <div class="col-sm-12">
                <p class="text">
                    Internet fraud continues to be on the rise and it can take place in many different ways. If you have had the misfortune of being of victim of these types of crimes, you are not alone. <a target="_blank" class="a-link" href="https://www.usa.gov/online-safety">More than a million people</a> are defrauded each year by Internet scammers in the United States and throughout the world.
                </p>

                <div class="flex-start direction-col gap-15 mt-20">
                    <div class="h-text mb-10">Some of the crimes perpetrated include:</div>
                    <div> <i class="fa fa-check c-green mr-8"></i> Credit card fraud </div>
                    <div> <i class="fa fa-check c-green mr-8"></i> Identity theft</div>
                    <div> <i class="fa fa-check c-green mr-8"></i> Internet auction fraud</div>
                    <div> <i class="fa fa-check c-green mr-8"></i> Investment fraud</div>
                    <div> <i class="fa fa-check c-green mr-8"></i> Nigerian letter fraud</div>

                </div>

                <p class="text">Falling victim to any of these can be embarrassing and frustrating, but you are not alone. Knowing how to report Internet fraud is the first step to helping authorities identify trends, develop cases against Internet criminals and work to prevent future crimes from occurring.</p>




                <div class="group-hh5d4a">
                    <div class="h-text mb-10">Organizations that Handle Internet Fraud</div>
                    <span class="text">If you are or suspect that you have been a victim of Internet fraud, there are agencies to turn to: </span>
                    <br>

                    <div class="box-hh5d4a">
                        <p class="text p"><span class="bold">Federal Trade Commission (FTC)</span> As the consumer protection agency for the United States, the FTC collects information regarding deceptive and fraudulent business practices. You can report your complaint using their <a href="https://reportfraud.ftc.gov/" class="a-link">online Complaint Assistant</a>.</p>

                    </div>

                    <div class="box-hh5d4a">
                        <p class="text p"><span>State Attorney General</span> Every state has their office of the Attorney General. The purpose of the Attorney General is to review complaints that are related to protecting consumers from fraud and unfair business practices.</p>
                    </div>

                    <div class="box-hh5d4a">
                        <p class="text p"><span class="bold">Internet Crime Complaint Center (IC3)</span> This agency was formed under a joint partnership between the FBI, the Bureau of Justice Assistance and the National White Collar Crime Center. Its mission is to receive internet fraud complaints, develop cases based on information gathered and make referrals to appropriate law agencies for cyber crimes. You can file your IC3 report online.</p>
                    </div>

                    <div class="box-hh5d4a">
                        <p class="text p">Better Business Bureau (BBB)While the BBB cannot take legal action against an Internet scammer or business that has committed a fraud, they keep record of reports from consumers that can accessed by all and can help prevent other consumers from falling victim to poor or unscrupulous business practices.</p>
                    </div>



                    <div class="css-0">

                        <p class="text css-0">You will also want to notify your credit card company, the bank that issued your credit card as they will require you to follow their processes for getting the fraudulent charges reversed. They may ask that you contact the merchant directly or even file a report with your local police department as part of the process.

                        </p>
                    </div>

                    <div class="css-0">

                        <p class="text css-0">It is also a good idea to notify and take advantage of the various monitoring services made available by the three major credit bureaus in order to detect signs of identity theft based on your credit history. Read more on <a class="a-link" href="/news_&amp;_blogs/5">Consumer Advocacy and Why You Need It.</a>.</p>
                    </div>

                </div>
            </div>
